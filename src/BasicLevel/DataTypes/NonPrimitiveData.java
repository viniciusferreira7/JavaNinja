package BasicLevel.DataTypes;

public class NonPrimitiveData {
    public static void main(String[] args) {
        // Non-primitive data: String, BasicLevel.MidLevel.Challenges.Array, Class and enum
        String name = "Naruto";
        String nameToUpperCase = name.toUpperCase();

        System.out.println("name = " + name);
        System.out.println("nameToUpperCase = " + nameToUpperCase);
    }
}
